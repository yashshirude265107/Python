#Create basic calculator

def add(num1, num2):
    res=num1+num2
    return res
finalAns = add(34, 67)
print("The addition is", finalAns)

def sub(num1, num2):
    res=num1-num2
    return res
finalAns = sub(34, 67)
print("The subtract is", finalAns)

def mul(num1, num2):
    res=num1*num2
    return res
finalAns = mul(34, 67)
print("The multiplication is", finalAns)

def div(num1, num2):
    res=num1/num2
    return res
finalAns = div(34, 67)
print("The division is", finalAns)

